package com.company.day003_value;

public class A009 {
	public static void main(String[] args) {
		
		boolean bl = true; System.out.println(bl);
		
		byte by = 1;       System.out.println(by);
		

		short sh = 2;      System.out.println(sh);
		
		int i = 4;         System.out.println(i);

		long l = 8L;       System.out.println(l);

		float fl = 3.14f;  System.out.println(fl);
		double d = 3.14;   System.out.println(d);

		char ch1 = 'A';    System.out.println(ch1);
		char ch2 = ' ';    System.out.println("|" + ch2 + "|" + "\t" + (int) ch2);
		
	}

}
